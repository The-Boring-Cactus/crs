namespace FunctEngine
{
    public class WhileLoopInstance
    {
        private int startLocation;
        private int endLocation;

        public int StartLocation
        {
            get
            {
                return startLocation;
            }
            set
            {
                startLocation = value;
            }
        }

        public int EndLocation
        {
            get
            {
                return endLocation;
            }
            set
            {
                endLocation = value;
            }
        }

    }
}
